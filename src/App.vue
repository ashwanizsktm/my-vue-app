<template>
    <div class="container">
        <Header @btn-click="toggleAddTask" title="Task tracker" :showAddTask="showAddTask" />
        <div v-if="showAddTask">
            <AddTask @add-task="addTask" />
        </div>
        <Tasks :tasks="tasks" @toggle-reminder="toggleReminder" @delete-task="deleteTask" />
    </div>
</template>

<script>
import Header from "./components/Header.vue";
import Tasks from "./components/Tasks.vue";
import AddTask from "./components/AddTask.vue";

// all global variable goes here
// const taskData;
export default {
    apiUrl: "http://localhost:5000",
    name: "App",
    components: {
        Header,
        Tasks,
        AddTask,
    },
    data() {
        return {
            tasks: [],
            showAddTask: false,
            /*eslint no-mixed-spaces-and-tabs: ["error", "smart-tabs"]*/
        };
    },

    methods: {
        toggleAddTask() {
            this.showAddTask = !this.showAddTask;
        },

        async addTask(task) {
            try {
                const res = await fetch(`http://localhost:5000/tasks`, {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                    },
                    body: JSON.stringify(task),
                });
                const data = await res.json();
                this.tasks = [...this.tasks, data]
            } catch (error) {
                console.error(error)
            }
        },

        async deleteTask(id) {
            if (confirm("Are you sure you want to delete?")) {
                try {
                    const res = await fetch(`http://localhost:5000/tasks/${id}`, {
                        method: "DELETE",
                    });
                    if (res.status === 200) this.tasks =  this.tasks.filter((task) => task.id !== id);
                } catch (error) {
                    console.error(error)
                }
            }
        },

        async toggleReminder(id) {
            try {
                const taskToToggle = await this.fetchTask(id);
                const uptTask = {...taskToToggle, reminder: !taskToToggle.reminder}
                const res = await fetch(`http://localhost:5000/tasks/${id}`, { 
                    method: "PUT",
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify(uptTask)
                });

                const data = await res.json();
                this.tasks = this.tasks.map((task) =>
                task.id === id ? { ...task, reminder: data.reminder } : task
            );
            } catch (error) {
                console.error(error)
            }
            
        },

        async fetchTasks() {
            try {
                const res = await fetch(`http://localhost:5000/tasks`);
                const data = await res.json();
                console.log(data);
                return data;
            } catch (error) {
                console.log(error);
            }
        },

        async fetchTask(id) {
            try {
                const res = await fetch(`http://localhost:5000/tasks/${id}`);
                const data = await res.json();
                return data;
            } catch (error) {
                console.log(error);
            }
        },
    },

    async created() {
        this.tasks = await this.fetchTasks();
        console.log(this.tasks);
    },
};
</script>


